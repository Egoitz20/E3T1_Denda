CREATE DEFINER=`root`@`localhost` PROCEDURE `BEZERO_EZABATU`(
    IN SARRERA_ID INT
)
BEGIN
    DECLARE EXIT HANDLER FOR SQLEXCEPTION
    BEGIN
        ROLLBACK;
        RESIGNAL;
    END;
    
    START TRANSACTION;
    
    DELETE FROM BEZERO WHERE ID = SARRERA_ID;
    DELETE FROM BEZERO_TELEFONO WHERE ID_BEZERO = SARRERA_ID;
    
    COMMIT;
    
END